num1 = int(input('Введите число 1:  '))
num2 = int(input('Введите число 2:  '))

print(num1 + num2)  # плюс
print(num1 - num2)  # минус
print(num1 * num2)  # умножить
print(num1 / num2)  # поделить
print(num1 // num2) # целочисленное деление
print(num1 % num2)  # остаток от деления
print(num1 ** num2) # возвести в степень